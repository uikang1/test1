package variable;

public class Var3 {
  public static void main(String[] args) {
    // 변수 선언 , 변수(그릇) 생성
    int a ;
    int b , c , d ; // 여러개 선언
    //값 할당 ( 재할당 )
    a = 10;
    b = 20;
    c = 30;
    d = 10000;
    // 비어있는 변수는 출력할수 없다
    System.out.println("a:" + a );
    System.out.println("b:" + b );
    System.out.println("c:" + c );
    System.out.println("d:" + d );
  }
}
